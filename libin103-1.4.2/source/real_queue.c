#include <stdlib.h>

#include "real_list.h"
#include "real_queue.h"

/*****************************************************************************
*                                                                            *
*  ------------------------------ queue_init ------------------------------  *
*                                                                            *
*****************************************************************************/

void real_queue_init (real_queue_t* queue) {
  real_list_init (queue);
}

/*****************************************************************************
*                                                                            *
*  ------------------------------ queue_destroy----------------------------  *
*                                                                            *
*****************************************************************************/

void real_queue_destroy (real_queue_t *queue) {
  real_list_destroy (queue);
}

/*****************************************************************************
*                                                                            *
*  ------------------------------ queue_size ------------------------------  *
*                                                                            *
*****************************************************************************/

int real_queue_size (real_queue_t * queue) {
  return real_list_size (queue);
}

/*****************************************************************************
*                                                                            *
*  ----------------------------- queue_enqueue ----------------------------  *
*                                                                            *
*****************************************************************************/

int real_queue_enqueue(real_queue_t *queue, double data) {
  /*  Enqueue the data.  */
  return real_list_ins_next(queue, real_list_tail(queue), data);
}

/*****************************************************************************
*                                                                            *
*  ----------------------------- queue_dequeue ----------------------------  *
*                                                                            *
*****************************************************************************/

/* User has to check that the queue is not empty */
int real_queue_dequeue(real_queue_t* queue, double* data) {
  /*  Dequeue the data.  */
  return real_list_rem_next(queue, NULL, data);
}

/*****************************************************************************
*                                                                            *
*  ------------------------------ queue_peek ------------------------------  *
*                                                                            *
*****************************************************************************/

/* User has to check that the queue is not empty */
double real_queue_peek(real_queue_t * queue) {
    return queue->head->data;
}
