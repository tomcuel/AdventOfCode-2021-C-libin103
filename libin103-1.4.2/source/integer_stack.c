#include <stdlib.h>

#include "integer_stack.h"

/*****************************************************************************
*                                                                            *
*  ------------------------------ stack_init ------------------------------  *
*                                                                            *
*****************************************************************************/

void integer_stack_init (integer_stack_t* stack) {
   integer_list_init (stack);
}

/*****************************************************************************
*                                                                            *
*  ------------------------------ stack_destroy----------------------------  *
*                                                                            *
*****************************************************************************/

void integer_stack_destroy (integer_stack_t *stack) {
  integer_list_destroy (stack);
}

/*****************************************************************************
*                                                                            *
*  ------------------------------ stack_size ------------------------------  *
*                                                                            *
*****************************************************************************/

int integer_stack_size (integer_stack_t * stack) {
  return integer_list_size (stack);
}

/*****************************************************************************
*                                                                            *
*  ------------------------------ stack_push ------------------------------  *
*                                                                            *
*****************************************************************************/

int integer_stack_push(integer_stack_t *stack, int data) {
  /*  Push the data onto the stack.  */
  return integer_list_ins_next(stack, NULL, data);
}

/*****************************************************************************
*                                                                            *
*  ------------------------------ stack_pop -------------------------------  *
*                                                                            *
*****************************************************************************/

/* User has to check that the queue is not empty */
int integer_stack_pop(integer_stack_t *stack, int* data) {
  /*  Pop the data off the stack.  */
  return integer_list_rem_next(stack, NULL, data);
}

/*****************************************************************************
*                                                                            *
*  ------------------------------ stack_peek ------------------------------  *
*                                                                            *
*****************************************************************************/

/* User has to check that the stack is not empty */
int integer_stack_peek(integer_stack_t * stack) {
    return stack->head->data;
}
