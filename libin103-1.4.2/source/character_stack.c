#include <stdlib.h>

#include "character_stack.h"

/*****************************************************************************
*                                                                            *
*  ------------------------------ stack_init ------------------------------  *
*                                                                            *
*****************************************************************************/

void character_stack_init (character_stack_t* stack) {
   character_list_init (stack);
}

/*****************************************************************************
*                                                                            *
*  ------------------------------ stack_destroy----------------------------  *
*                                                                            *
*****************************************************************************/

void character_stack_destroy (character_stack_t *stack) {
  character_list_destroy (stack);
}

/*****************************************************************************
*                                                                            *
*  ------------------------------ stack_size ------------------------------  *
*                                                                            *
*****************************************************************************/

int character_stack_size (character_stack_t * stack) {
  return character_list_size (stack);
}

/*****************************************************************************
*                                                                            *
*  ------------------------------ stack_push ------------------------------  *
*                                                                            *
*****************************************************************************/

int character_stack_push(character_stack_t *stack, char data) {
  /*  Push the data onto the stack.  */
  return character_list_ins_next(stack, NULL, data);
}

/*****************************************************************************
*                                                                            *
*  ------------------------------ stack_pop -------------------------------  *
*                                                                            *
*****************************************************************************/

/* User has to check that the queue is not empty */
int character_stack_pop(character_stack_t *stack, char* data) {
  /*  Pop the data off the stack.  */
  return character_list_rem_next(stack, NULL, data);
}

/*****************************************************************************
*                                                                            *
*  ------------------------------ stack_peek ------------------------------  *
*                                                                            *
*****************************************************************************/

/* User has to check that the stack is not empty */
char character_stack_peek(character_stack_t * stack) {
    return stack->head->data;
}
