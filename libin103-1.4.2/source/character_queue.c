#include <stdlib.h>

#include "character_list.h"
#include "character_queue.h"

/*****************************************************************************
*                                                                            *
*  ------------------------------ queue_init ------------------------------  *
*                                                                            *
*****************************************************************************/

void character_queue_init (character_queue_t* queue) {
  character_list_init (queue);
}

/*****************************************************************************
*                                                                            *
*  ------------------------------ queue_destroy----------------------------  *
*                                                                            *
*****************************************************************************/

void character_queue_destroy (character_queue_t *queue) {
  character_list_destroy (queue);
}

/*****************************************************************************
*                                                                            *
*  ------------------------------ queue_size ------------------------------  *
*                                                                            *
*****************************************************************************/

int character_queue_size (character_queue_t * queue) {
  return character_list_size (queue);
}

/*****************************************************************************
*                                                                            *
*  ----------------------------- queue_enqueue ----------------------------  *
*                                                                            *
*****************************************************************************/

int character_queue_enqueue(character_queue_t *queue, char data) {
  /*  Enqueue the data.  */
  return character_list_ins_next(queue, character_list_tail(queue), data);
}

/*****************************************************************************
*                                                                            *
*  ----------------------------- queue_dequeue ----------------------------  *
*                                                                            *
*****************************************************************************/

/* User has to check that the queue is not empty */
int character_queue_dequeue(character_queue_t* queue, char* data) {
  /*  Dequeue the data.  */
  return character_list_rem_next(queue, NULL, data);
}

/*****************************************************************************
*                                                                            *
*  ------------------------------ queue_peek ------------------------------  *
*                                                                            *
*****************************************************************************/

/* User has to check that the queue is not empty */
char character_queue_peek(character_queue_t * queue) {
    return queue->head->data;
}
