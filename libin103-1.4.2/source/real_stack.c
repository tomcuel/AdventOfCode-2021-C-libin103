#include <stdlib.h>

#include "real_stack.h"

/*****************************************************************************
*                                                                            *
*  ------------------------------ stack_init ------------------------------  *
*                                                                            *
*****************************************************************************/

void real_stack_init (real_stack_t* stack) {
   real_list_init (stack);
}

/*****************************************************************************
*                                                                            *
*  ------------------------------ stack_destroy----------------------------  *
*                                                                            *
*****************************************************************************/

void real_stack_destroy (real_stack_t *stack) {
  real_list_destroy (stack);
}

/*****************************************************************************
*                                                                            *
*  ------------------------------ stack_size ------------------------------  *
*                                                                            *
*****************************************************************************/

int real_stack_size (real_stack_t * stack) {
  return real_list_size (stack);
}

/*****************************************************************************
*                                                                            *
*  ------------------------------ stack_push ------------------------------  *
*                                                                            *
*****************************************************************************/

int real_stack_push(real_stack_t *stack, double data) {
  /*  Push the data onto the stack.  */
  return real_list_ins_next(stack, NULL, data);
}

/*****************************************************************************
*                                                                            *
*  ------------------------------ stack_pop -------------------------------  *
*                                                                            *
*****************************************************************************/

/* User has to check that the queue is not empty */
int real_stack_pop(real_stack_t *stack, double* data) {
  /*  Pop the data off the stack.  */
  return real_list_rem_next(stack, NULL, data);
}

/*****************************************************************************
*                                                                            *
*  ------------------------------ stack_peek ------------------------------  *
*                                                                            *
*****************************************************************************/

/* User has to check that the stack is not empty */
double real_stack_peek(real_stack_t * stack) {
    return stack->head->data;
}
